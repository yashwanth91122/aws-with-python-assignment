a = 10

# Subtraction assignment
a -= 2
print(a)  # 98

# Multiplication assignment
a *= 3
print(a)  # 294

# Division assignment
a /= 4
print(a)  # 73.5

# Floor division assignment
a //= 2
print(a)  # 36.0

# Modulus assignment
a %= 2
print(a)  # 0.0

# Exponentiation assignment
a **= 3
print(a)  # 0.0

# Bitwise AND assignment
a = 5  # 5 in binary is 0101
a &= 3  # 3 in binary is 0011
print(a)  # 1 (binary 0001)

# Bitwise OR assignment
a |= 2  # 2 in binary is 0010
print(a)  # 3 (binary 0011)

# Bitwise XOR assignment
a ^= 1  # 1 in binary is 0001
print(a)  # 2 (binary 0010)

# Bitwise left shift assignment
a <<= 1
print(a)  # 4 (binary 0100)

# Bitwise right shift assignment
a >>= 1
print(a)  # 2 (binary 0010)